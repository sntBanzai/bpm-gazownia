@Entity
@Table(name = "path_property_type")
@NamedQueries({
    @NamedQuery(name = "PathPropertyType.findAll", query = "SELECT p FROM PathPropertyType p")})
public class PathPropertyType implements Serializable {

    private static final long serialVersionUID = 1L;
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Basic(optional = false)
    @Column(name = "id")
    private Long id;
    @Basic(optional = false)
    @NotNull
    @Size(min = 1, max = 255)
    @Column(name = "name")
    private String name;

    public PathPropertyType() {
    }

    public PathPropertyType(Long id) {
        this.id = id;
    }

    public PathPropertyType(Long id, String name) {
        this.id = id;
        this.name = name;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof PathPropertyType)) {
            return false;
        }
        PathPropertyType other = (PathPropertyType) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return getClass().getCanonicalName() + "[ id=" + id + " ]";
    }
    
    public static PathPropertyType create(String name){
        PathPropertyType retVal = new PathPropertyType();
        retVal.setName(name);
        return retVal;
    }

}
